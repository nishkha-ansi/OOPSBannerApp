class OopsWord {
    public static void main(String[] args) {
        // Each letter (O, O, P, S) is 9 characters wide, aligned properly
        System.out.println("  *****   " + "  *****   " + " ******** " + " ******** ");
        System.out.println(" *     *  " + " *     *  " + " *       *" + " *        ");
        System.out.println(" *     *  " + " *     *  " + " *       *" + " *        ");
        System.out.println(" *     *  " + " *     *  " + " ******** " + " ******** ");
        System.out.println(" *     *  " + " *     *  " + " *         " + "       *  ");
        System.out.println(" *     *  " + " *     *  " + " *         " + "       *  ");
        System.out.println("  *****   " + "  *****   " + " *         " + " ******** ");
    }
}